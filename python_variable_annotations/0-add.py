#!/usr/bin/env python3
def add(a: float, b: float) -> float:
    """
    Module: 0-add

    This module provides a function to add two floating-point numbers.

    Functions:
        add(a: float, b: float) -> float:
            Returns the sum of two floating-point numbers.

    Example:
        >>> add(2.5, 3.1)
        5.6
    """
    """Add two numbers together."""
    return a + b
